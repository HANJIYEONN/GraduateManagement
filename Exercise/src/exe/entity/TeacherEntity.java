package exe.entity;

public class TeacherEntity {
	private String teacherId;
	private String teacherPass;
	private String teacherName;
	private String department;
	private String teacherAddr;
	public String getTeacherId() {
		return teacherId;
	}
	public void setTeacherId(String teacherId) {
		this.teacherId = teacherId;
	}
	public String getTeacherPass() {
		return teacherPass;
	}
	public void setTeacherPass(String teacherPass) {
		this.teacherPass = teacherPass;
	}
	public String getTeacherName() {
		return teacherName;
	}
	public void setTeacherName(String teacherName) {
		this.teacherName = teacherName;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getTeacherAddr() {
		return teacherAddr;
	}
	public void setTeacherAddr(String teacherAddr) {
		this.teacherAddr = teacherAddr;
	}
	


}
